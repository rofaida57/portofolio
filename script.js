        // Dark mode and language toggle
        const themeToggle = document.getElementById('theme-toggle');
        const languageToggle = document.getElementById('language-toggle');
        const langIndicator = document.getElementById('lang-indicator');
        const body = document.body;
        
        // Check user preferences
        const savedTheme = localStorage.getItem('theme');
        const savedLanguage = localStorage.getItem('language') || 'en';
        
        // Apply saved preferences
        if (savedTheme === 'dark') {
            body.classList.add('dark-mode');
            themeToggle.innerHTML = '<i class="fas fa-sun"></i>';
        }
        
        if (savedLanguage === 'es') {
            applyTranslation('es');
            langIndicator.textContent = 'ES';
        } else {
            applyTranslation('en');
            langIndicator.textContent = 'EN';
        }
        
        // Toggle dark mode
        themeToggle.addEventListener('click', () => {
            body.classList.toggle('dark-mode');
            
            if (body.classList.contains('dark-mode')) {
                localStorage.setItem('theme', 'dark');
                themeToggle.innerHTML = '<i class="fas fa-sun"></i>';
            } else {
                localStorage.setItem('theme', 'light');
                themeToggle.innerHTML = '<i class="fas fa-moon"></i>';
            }
        });
        
        // Toggle language
        languageToggle.addEventListener('click', () => {
            const currentLang = langIndicator.textContent === 'EN' ? 'en' : 'es';
            const newLang = currentLang === 'en' ? 'es' : 'en';
            
            // Add translation effect
            const elements = document.querySelectorAll('h1, h2, h3, p, span, a, li, button, label');
            elements.forEach(el => {
                el.classList.add('translate-transition', 'translating');
            });
            
            setTimeout(() => {
                applyTranslation(newLang);
                langIndicator.textContent = newLang === 'en' ? 'EN' : 'ES';
                localStorage.setItem('language', newLang);
                
                // Remove translation effect
                setTimeout(() => {
                    elements.forEach(el => {
                        el.classList.remove('translating');
                        el.classList.add('translated');
                    });
                    
                    setTimeout(() => {
                        elements.forEach(el => {
                            el.classList.remove('translate-transition', 'translated');
                        });
                    }, 500);
                }, 50);
            }, 300);
        });
        
        // Apply translation function
        function applyTranslation(lang) {
            const elements = document.querySelectorAll('[id]');
            elements.forEach(element => {
                const key = element.id;
                if (translations[lang] && translations[lang][key]) {
                    element.textContent = translations[lang][key];
                }
            });
        }
        
        // Smooth scrolling
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function(e) {
                e.preventDefault();
                
                const targetId = this.getAttribute('href');
                const targetElement = document.querySelector(targetId);
                
                window.scrollTo({
                    top: targetElement.offsetTop - 100,
                    behavior: 'smooth'
                });
            });
        });
        
        // Scroll effects
        window.addEventListener('scroll', () => {
            const sections = document.querySelectorAll('section');
            const navLinks = document.querySelectorAll('.nav-links a');
            
            let current = '';
            
            sections.forEach(section => {
                const sectionTop = section.offsetTop;
                const sectionHeight = section.clientHeight;
                
                if (pageYOffset >= sectionTop - 300) {
                    current = section.getAttribute('id');
                }
            });
            
            navLinks.forEach(link => {
                link.classList.remove('active');
                if (link.getAttribute('href').includes(current)) {
                    link.classList.add('active');
                }
            });
        });